int	ft_isprime(int n)
{
	int	i;

	i = 2;
	while (i * i <= n && n % i != 0)
		i++;
	if (n != 2 && (n % i == 0 || n < 2))
		i = 0;
	else
		i = 1;
	return (i);
}
